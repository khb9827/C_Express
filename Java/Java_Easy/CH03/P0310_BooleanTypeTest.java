
public class P0310_BooleanTypeTest {
	public static void main(String[] args) {
		boolean a = true; // 논리형 변수에 true값을 저장
		System.out.println("boolean 변수 a의 값은 : " + a);
		boolean b = 10 > 20; // 논리형 변수에 10>20의 결과를 저장
		System.out.println("boolean 변수 b의 값은 : " + b);
		boolean c = a; // 논리형 변수에 논리형 변수값을 저장
		System.out.println("boolean 변수 c의 값은 : " + c);
	}
}
